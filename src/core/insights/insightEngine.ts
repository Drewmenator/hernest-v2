export async function saveInsights(userId: string, insights: RichInsight[]): Promise<void> {
  try {
    await saveData(userId, "household_insights_v2", {
      insights,
      updatedAt: new Date().toISOString(),
    });
  } catch (e) {
    console.error("[InsightEngine] save failed:", e);
  }
}

export async function loadInsights(userId: string): Promise<RichInsight[]> {
  try {
    const data = await loadData(userId, "household_insights_v2");
    return (data?.insights as RichInsight[]) || [];
  } catch {
    return [];
  }
}

