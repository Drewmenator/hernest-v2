// ─── HerNest Unified Insight Engine ──────────────────────────────
// Generates, ranks, filters, deduplicates, and manages lifecycle
// for household insights across all modules.
//
// Philosophy:
// - Rules-based first (fast, explainable, no AI cost)
// - AI only for synthesis, wording, cross-module patterns
// - Calm over noise — every insight must earn its place
// - State-aware — overloaded households see fewer insights
//
// Extends existing HouseholdInsight type from store.ts

