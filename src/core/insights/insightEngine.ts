export type InsightType =
  | "risk"
  | "opportunity"
  | "reminder"
  | "pattern"
  | "recommendation"
  | "forecast"
  | "celebration";

export type InsightSeverity = "low" | "medium" | "high";

export type InsightStatus =
  | "active"
  | "viewed"
  | "acted_on"
  | "snoozed"
  | "dismissed"
  | "expired";

export type InsightAction = {
  label: string;
  actionType:
    | "open_module"
    | "create_task"
    | "adjust_budget"
    | "create_goal"
    | "ask_nora"
    | "schedule_review"
    | "dismiss"
    | "snooze";
  targetModule?: string;
};

export interface RichInsight extends HouseholdInsight {
  // Extended fields (all optional for backward compatibility)
  title?: string;
  summary?: string;
  type?: InsightType;
  severity?: InsightSeverity;
  status?: InsightStatus;
  actions?: InsightAction[];
  expiresAt?: string;
  snoozedUntil?: string;
  explanation?: {
    contextUsed: string[];
    assumptions: string[];
    confidenceReason: string;
  };
  ruleId?: string;        // set if generated by a rule
  scored?: number;        // final ranking score 0–100
}

export interface InsightScore {
  urgency:       number; // 0–100 time sensitivity
  importance:    number; // 0–100 consequence if ignored
  confidence:    number; // 0–100 quality of evidence
  actionability: number; // 0–100 can user do something?
  novelty:       number; // 0–100 not recently shown
  stateFit:      number; // 0–100 matches household state
}

