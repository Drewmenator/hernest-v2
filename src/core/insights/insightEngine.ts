// SCORING ENGINE
// Ranks insights by weighted score
// ═══════════════════════════════════════════════════════════════════

function scoreInsight(
  insight: RichInsight,
  householdState: HouseholdStateResult | null,
  existing: RichInsight[]
): number {
  const urgencyMap: Record<InsightSeverity, number> = {
    high: 80, medium: 50, low: 20
  };
  const urgency       = urgencyMap[insight.severity ?? "medium"];
  const importance    = insight.confidenceLevel ?? 50;
  const confidence    = insight.confidenceLevel ?? 50;
  const actionability = (insight.actions?.length ?? 0) > 0 ? 75 : 30;

  // Novelty: penalize if similar insight was recently shown
  const recentSimilar = existing.filter(e =>
    e.category === insight.category &&
    e.ruleId === insight.ruleId &&
    e.status !== "dismissed" &&
    new Date(e.createdAt).getTime() > Date.now() - 3 * 24 * 60 * 60 * 1000
  ).length;
  const novelty = recentSimilar > 0 ? 10 : 80;

  // State fit: does this insight match the current household state?
  let stateFit = 50;
  if (householdState) {
    const primaryState = householdState.primary.state;
    const catStateMap: Partial<Record<InsightCategory, string[]>> = {
      spending:       ["financial_pressure", "busy", "overloaded"],
      savings:        ["calm", "financial_pressure"],
      cashflow:       ["financial_pressure"],
      stress:         ["overloaded", "busy"],
      scheduling:     ["busy", "overloaded", "travel_prep", "school_transition"],
      opportunity:    ["calm", "recovery"],
      decision:       ["decision_heavy"],
    };
    const goodStates = catStateMap[insight.category] ?? [];
    stateFit = goodStates.includes(primaryState) ? 90 : 40;
  }

  return Math.round(
    urgency       * 0.25 +
    importance    * 0.25 +
    confidence    * 0.20 +
    actionability * 0.15 +
    stateFit      * 0.10 +
    novelty       * 0.05
  );
}

// ═══════════════════════════════════════════════════════════════════
// STATE-AWARE FILTER
// Limits insight count and types based on household state
// ═══════════════════════════════════════════════════════════════════

function filterByState(
  insights: RichInsight[],
  householdState: HouseholdStateResult | null
): RichInsight[] {
  if (!householdState) return insights.slice(0, 3);

  const state = householdState.primary.state;

  switch (state) {
    case "overloaded":
      // Max 2 insights, only high severity
      return insights
        .filter(i => i.severity === "high" || i.type === "recommendation")
        .slice(0, 2);

    case "busy":
      // Max 3 insights, skip low-severity optimizations
      return insights
        .filter(i => i.severity !== "low" || i.type === "reminder")
        .slice(0, 3);

    case "calm":
    case "recovery":
      // Show opportunities and planning insights freely
      return insights.slice(0, 4);

    case "financial_pressure":
      // Prioritize financial insights
      return [
        ...insights.filter(i => ["spending", "savings", "cashflow", "debt"].includes(i.category)),
        ...insights.filter(i => !["spending", "savings", "cashflow", "debt"].includes(i.category)),
      ].slice(0, 3);

    case "travel_prep":
      return [
        ...insights.filter(i => i.ruleId === "travel_prep_incomplete"),
        ...insights.filter(i => i.ruleId !== "travel_prep_incomplete"),
      ].slice(0, 3);

    default:
      return insights.slice(0, 3);
  }
}

