export function expireOldInsights(insights: RichInsight[]): RichInsight[] {
  const now = Date.now();
  return insights.map(i => {
    if (i.expiresAt && new Date(i.expiresAt).getTime() < now) {
      return { ...i, status: "expired" as InsightStatus };
    }
    return i;
  });
}

export function markInsightViewed(insights: RichInsight[], id: string): RichInsight[] {
  return insights.map(i => i.id === id ? { ...i, status: "viewed" as InsightStatus } : i);
}

export function markInsightActedOn(insights: RichInsight[], id: string): RichInsight[] {
  return insights.map(i => i.id === id ? { ...i, status: "acted_on" as InsightStatus } : i);
}

export function dismissInsight(insights: RichInsight[], id: string): RichInsight[] {
  return insights.map(i =>
    i.id === id ? { ...i, status: "dismissed" as InsightStatus, dismissed: true } : i
  );
}

export function snoozeInsight(insights: RichInsight[], id: string, days = 3): RichInsight[] {
  const until = new Date(Date.now() + days * 24 * 60 * 60 * 1000).toISOString();
  return insights.map(i =>
    i.id === id ? { ...i, status: "snoozed" as InsightStatus, snoozedUntil: until } : i
  );
}

export function getActiveInsights(insights: RichInsight[]): RichInsight[] {
  const now = Date.now();
  return insights.filter(i => {
    if (i.status === "dismissed" || i.dismissed) return false;
    if (i.status === "expired") return false;
    if (i.status === "snoozed" && i.snoozedUntil && new Date(i.snoozedUntil).getTime() > now) return false;
    return true;
  });
}

// ═══════════════════════════════════════════════════════════════════
// MAIN ENGINE
// ═══════════════════════════════════════════════════════════════════

export function generateRuleBasedInsights(
  ctx: AppContext,
  existing: RichInsight[],
  householdState: HouseholdStateResult | null
): RichInsight[] {
  const generated: RichInsight[] = [];

  for (const rule of RULES) {
    // Skip if on cooldown
    if (isOnCooldown(rule.id, existing)) continue;

    try {
      const insight = rule.check(ctx);
      if (insight) generated.push(insight);
    } catch (e) {
      console.warn(`[InsightEngine] Rule ${rule.id} failed:`, e);
    }
  }

  // Score each insight
  const scored = generated.map(i => ({
    ...i,
    scored: scoreInsight(i, householdState, existing),
  }));

  // Sort by score
  scored.sort((a, b) => (b.scored ?? 0) - (a.scored ?? 0));

  // Filter by household state
  return filterByState(scored, householdState);
}

// ═══════════════════════════════════════════════════════════════════
// AI SYNTHESIS
// Called only when rule-based insights need enrichment or
// when cross-module patterns require reasoning
// ═══════════════════════════════════════════════════════════════════

export async function enrichInsightsWithAI(
  insights: RichInsight[],
  ctx: AppContext
): Promise<RichInsight[]> {
  if (insights.length === 0) return insights;

  // Only enrich cross-module insights — single-module insights are fine as-is
  const needsEnrichment = insights.filter(i =>
    (i.sourceModules?.length ?? 0) >= 2 && i.type === "pattern"
  );
  if (needsEnrichment.length === 0) return insights;

  const sys = `You are HerNest's household intelligence engine.
Improve the wording of these household insights to be warmer, more specific, and more actionable.
Do not change the facts or add invented data. Only improve clarity and tone.
Return ONLY valid JSON array of the same objects with improved title, summary, observation, whyItMatters, and recommendation fields.
Keep responses concise — each field under 2 sentences.`;

  try {
    const improved = await aiJSON<RichInsight[]>(
      sys,
      JSON.stringify(needsEnrichment.map(i => ({
        id: i.id,
        title: i.title,
        summary: i.summary,
        observation: i.observation,
        whyItMatters: i.whyItMatters,
        recommendation: i.recommendation,
      }))),
      "household_cfo",
      needsEnrichment
    );

    if (!Array.isArray(improved)) return insights;

    // Merge improved wording back
    return insights.map(i => {
      const enriched = improved.find(e => e.id === i.id);
      if (!enriched) return i;
      return {
        ...i,
        title:          enriched.title          ?? i.title,
        summary:        enriched.summary        ?? i.summary,
        observation:    enriched.observation    ?? i.observation,
        whyItMatters:   enriched.whyItMatters   ?? i.whyItMatters,
        recommendation: enriched.recommendation ?? i.recommendation,
      };
    });
  } catch (e) {
    console.warn("[InsightEngine] AI enrichment failed (non-fatal):", e);
    return insights;
  }
}

