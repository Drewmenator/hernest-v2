// TOP INSIGHTS SELECTOR
// Returns the best 1–3 insights for dashboard display
// ═══════════════════════════════════════════════════════════════════

export function getTopInsights(
  insights: RichInsight[],
  max = 3
): RichInsight[] {
  return getActiveInsights(expireOldInsights(insights))
    .sort((a, b) => (b.scored ?? b.confidenceLevel ?? 0) - (a.scored ?? a.confidenceLevel ?? 0))
    .slice(0, max);
}

// ═══════════════════════════════════════════════════════════════════
// NORA EXPLANATION
// Generates a human-readable explanation of why an insight appeared
// ═══════════════════════════════════════════════════════════════════

export function explainInsight(insight: RichInsight): string {
  const sources = insight.explanation?.contextUsed ?? insight.sourceModules ?? [];
  const assumptions = insight.explanation?.assumptions ?? [];

  const lines = [
    `Nora noticed this because of: ${sources.join(", ")}.`,
    insight.explanation?.confidenceReason
      ? `Confidence: ${insight.explanation.confidenceReason}.`
      : "",
    assumptions.length
      ? `Assumptions: ${assumptions.join("; ")}.`
      : "",
  ].filter(Boolean);

  return lines.join(" ");
}
